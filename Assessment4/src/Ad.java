
public class Ad {
  String answer;
  String postingTitle;
  String postingCity;
  String description;
  String name;
  String email;
  String phone;
public Ad(String answer, String postingTitle, String postingCity, String description, String name, String email,
		String phone) {
	super();
	this.answer = answer;
	this.postingTitle = postingTitle;
	this.postingCity = postingCity;
	this.description = description;
	this.name = name;
	this.email = email;
	this.phone = phone;
}
public String getAnswer() {
	return answer;
}
public void setAnswer(String answer) {
	this.answer = answer;
}
public String getPostingTitle() {
	return postingTitle;
}
public void setPostingTitle(String postingTitle) {
	this.postingTitle = postingTitle;
}
public String getPostingCity() {
	return postingCity;
}
public void setPostingCity(String postingCity) {
	this.postingCity = postingCity;
}
public String getDescription() {
	return description;
}
public void setDescription(String description) {
	this.description = description;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}
public String getPhone() {
	return phone;
}
public void setPhone(String phone) {
	this.phone = phone;
}
  
  
}
